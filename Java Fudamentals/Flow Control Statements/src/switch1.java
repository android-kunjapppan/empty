class BC{
	public static void main(String args[])
	{
		String s=args[0];
		char c= s.charAt(0);
		switch(c)
		{
		case 'R':
			System.out.println("Red");
			break;
		case 'B':
			System.out.println("Blue");
			break;
		case 'G':
			System.out.println("March");
			break;
		case 'O':
			System.out.println("April");
			break;
		case 'Y':
			System.out.println("May");
			break;
		case 'W':
			System.out.println("May");
			break;
		default:
			System.out.println("Invalid Code");
			
}
}
}